async def seed_database():
    engine = create_async_engine(settings.DATABASE_URL)
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    Session = async_sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)
    async with Session() as db:
        from sqlalchemy import select

        # Admin user
        existing = await db.execute(select(User).where(User.email == settings.ADMIN_EMAIL))
        if not existing.scalar_one_or_none():
            admin = User(
                email=settings.ADMIN_EMAIL,
                username="admin",
                hashed_password=hash_password(settings.ADMIN_PASSWORD),
                is_admin=True,
            )
            db.add(admin)

        # Continents
        continent_map = {}
        for name, code, desc in CONTINENTS:
            existing = await db.execute(select(Continent).where(Continent.code == code))
            c = existing.scalar_one_or_none()
            if not c:
                c = Continent(name=name, code=code, description=desc)
                db.add(c)
                await db.flush()
            continent_map[code] = c

        # Conservation statuses
        cs_map = {}
        for code, name, severity in CONSERVATION_STATUSES:
            existing = await db.execute(select(ConservationStatus).where(ConservationStatus.code == code))
            cs = existing.scalar_one_or_none()
            if not cs:
                cs = ConservationStatus(code=code, name=name, severity=severity)
                db.add(cs)
                await db.flush()
            cs_map[code] = cs

        # Behaviors
        for cat, label in BEHAVIORS:
            existing = await db.execute(select(Behavior).where(Behavior.category == cat))
            if not existing.scalar_one_or_none():
                db.add(Behavior(category=cat, label=label))

        await db.flush()

        # Countries
        for name, code, cont_code, capital, region, subregion, pop, flag_url in SEED_COUNTRIES:
            existing = await db.execute(select(Country).where(Country.code == code))
            if not existing.scalar_one_or_none():
                continent = continent_map.get(cont_code)
                db.add(Country(
                    name=name,
                    code=code,
                    continent_id=continent.id if continent else None,
                    capital=capital,
                    region=region,
                    subregion=subregion,
                    population=pop,
                    flag_url=flag_url,
                ))
        await db.flush()

        # Build country_map for animal-country linking
        country_map = {}
        result = await db.execute(select(Country))
        for c in result.scalars().all():
            country_map[c.code] = c

        # Animals
        for data in SEED_ANIMALS:
            existing = await db.execute(select(Animal).where(Animal.slug == slugify(data["common_name"])))
            if existing.scalar_one_or_none():
                continue

            cs = cs_map.get(data.get("conservation_code"))
            continent = continent_map.get(data.get("continent_code"))

            animal = Animal(
                slug=slugify(data["common_name"]),
                common_name=data["common_name"],
                scientific_name=data.get("scientific_name"),
                class_name=data.get("class_name"),
                order_name=data.get("order_name"),
                family_name=data.get("family_name"),
                genus=data.get("genus"),
                diet=data.get("diet"),
                lifespan=data.get("lifespan"),
                average_weight=data.get("average_weight"),
                average_length=data.get("average_length"),
                habitat_summary=data.get("habitat_summary"),
                description=data.get("description"),
                behavior_summary=data.get("behavior_summary"),
                reproduction=data.get("reproduction"),
                predators=data.get("predators"),
                prey=data.get("prey"),
                social_structure=data.get("social_structure"),
                activity_period=data.get("activity_period"),
                environment_type=data.get("environment_type"),
                biome=data.get("biome"),
                fun_facts=data.get("fun_facts"),
                wiki_summary=data.get("wiki_summary"),
                hero_image_url=data.get("hero_image_url"),
                thumbnail_url=data.get("thumbnail_url"),
                popularity=data.get("popularity", 50),
                conservation_status_id=cs.id if cs else None,
                continent_id=continent.id if continent else None,
                kingdom="Animalia",
            )
            db.add(animal)
            await db.flush()

            # Hero image
            if data.get("hero_image_url"):
                db.add(Image(animal_id=animal.id, url=data["hero_image_url"], is_hero=True, source="wikimedia"))

            # Fun fact
            if data.get("fun_facts"):
                db.add(Fact(animal_id=animal.id, content=data["fun_facts"], source="seed"))

            # Occurrences
            for lat, lon in data.get("occurrences", []):
                db.add(Occurrence(animal_id=animal.id, latitude=lat, longitude=lon, source_name="seed"))

            animal.observation_count = len(data.get("occurrences", []))

            # Link animal to countries
            slug = slugify(data["common_name"])
            for cc in ANIMAL_COUNTRY_MAP.get(slug, []):
                country_obj = country_map.get(cc)
                if country_obj:
                    await db.execute(
                        animal_countries_table.insert().values(
                            animal_id=animal.id, country_id=country_obj.id
                        )
                    )

        await db.commit()
    await engine.dispose()
    print("Seed complete!")


if __name__ == "__main__":
    asyncio.run(seed_database())
